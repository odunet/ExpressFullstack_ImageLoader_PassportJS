const mongoose = require('mongoose');

//Create newSchema (async)
const oauthSchema = new mongoose.Schema({
  googleId: {
    type: String,
    required: true,
  },
  displayName: {
    type: String,
    required: true,
  },
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  image: {
    type: String,
  },
  created_at: {
    type: Date,
    default: Date.now(),
  },
});

const oauthuser = mongoose.model('oauthuser', oauthSchema);

module.exports = oauthuser;
